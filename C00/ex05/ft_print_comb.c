#include <unistd.h>

void	ft_print_comb(void)
{
	char	l;
	char	m;
	char	n;

	l = '0';
	while (l <= '7')
	{
		m = l + 1;
		while (m <= '8')
		{
			n = m + 1;
			while (n <= '9')
			{
				write(1, &l, 1);
				write(1, &m, 1);
				write(1, &n, 1);
				if (l != '7')
					write(1, ", ", 2);
				n++;
			}
			m++;
		}
		l++;
	}
}
