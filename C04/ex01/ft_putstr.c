#include <unistd.h>
void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
		write(1, &str[i++], 1);
}
int main()
{
	char h[] = "Hello guys! how are you doing ?\n";
	ft_putstr(h);
	return 0;
}
